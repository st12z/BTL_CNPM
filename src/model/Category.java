package model;

/**
 *
 * @author T
 */
public class Category {
    private int id;
    private String name;

    public Category() {
    }

    public Category(int id, String name) {
        this.id = id;
        this.name = name;
    }

    @Override
    public String toString() {
        return "Category{" + "id=" + id + ", name=" + name + '}';
    }
    public Object[] toObject() {
        return new Object[]{
            id,
            name,
        };
    }
}
